public class FemTabel {
    static void FemTabelWhile() {
        int i = 0;
        while ( i <= 45) {
            System.out.println(i);
            i = i + 5; 
        }
    }
    static void FemTabelFor() {
        for (int j = 0 ; j <= 45 ; j = j + 5) {
            System.out.println(j);
        }
    }
}