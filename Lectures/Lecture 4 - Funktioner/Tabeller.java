public class Tabeller {
    static void tiTabel() {
        int t = 10;
        for (int i = 1; i <= 10; i = i + 1) {
            System.out.println(i * t);
        }
    }
    static void fireTabel() {
        int t = 4;
        for (int i = 1; i <= 10; i = i + 1) {
            System.out.println(i * t);
        }
    }
    static void syvTabel() {
        int t = 7;
        for (int i = 1; i <= 10; i = i + 1) {
            System.out.println(i * t);
        }
    }
    static void tabeller() {
        tiTabel();
        syvTabel();
        fireTabel();
    }
}